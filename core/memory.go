package core

const MEMORY_SIZE = 1024 * 4

type memory struct {
	storage [MEMORY_SIZE]uint8
}

func (m *memory) Initialize() {
	for i := 0; i < MEMORY_SIZE; i++ {
		m.storage[i] = 0
	}
	m.initBuiltInFont()
}

func (m *memory) GetBuiltInFontAddr(n uint8) uint8 {
	return 0x050 + n*5
}

func (m *memory) initBuiltInFont() {
	for i := 0; i < 80; i++ {
		m.storage[0x050+i] = fonts[i]
	}
}

